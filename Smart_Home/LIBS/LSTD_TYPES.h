/* Header file guard */
#ifndef _LSTDTYPES_H_
#define _LSTDTYPES_H_

/*! Integer 8 bits unsigned data type */
typedef unsigned char       u8_t;

/*! Integer 8 bits signed data type */
typedef signed char         s8_t;

/*! Integer 16 bits unsigned data type */
typedef unsigned short int u16_t;

/*! Integer 16 bits signed data type */
typedef signed short int   s16_t;

/*! Integer 32 bits unsigned data type */
typedef unsigned long int  u32_t;

/*! Integer 32 bits signed data type */
typedef signed long int    s32_t;

/*! Float 32 bits data type */
typedef float              f32_t;

/*! Float 64 bits data type */
typedef double             f64_t;

#endif /*_LSTDTYPES_H_*/
